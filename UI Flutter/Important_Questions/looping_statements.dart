void main() {
  print("Using for loop:");
  for (int i = 1; i <= 5; i++) {
    print("Count: $i");
  }

  print("\nUsing while loop:");
  int j = 1;
  while (j <= 3) {
    print("Number: $j");
    j++;
  }
}

// Output
Using for loop:
Count: 1
Count: 2
Count: 3
Count: 4
Count: 5

Using while loop:
Number: 1
Number: 2
Number: 3

